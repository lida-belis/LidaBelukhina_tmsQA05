public class Task_4 {
    public static void main(String[] args) {
        int A = 2;
        int B = 4;
        int sum = 0;
        for (int i = 0; i < B; i++) {
            sum = sum + A;
        }
        System.out.println("A * B = " + sum);
    }

}
